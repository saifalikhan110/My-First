    private void facComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_facComboActionPerformed
        // TODO add your handling code here:
        
         FacultyBean bean=(FacultyBean)this.facCombo.getSelectedItem();
         if(bean==null) return;
        try{
           deptCombo.removeAllItems();
             
             
            Vector <DepartmentBean> department=DatabaseManager.getDepartment(bean.getFacId());
            
            for(DepartmentBean dp:department){
                
                this.deptCombo.addItem(dp);
            }
            
            
            getProgram();
            
           
   
           
        }
        
        catch(Exception e){
            
            e.printStackTrace();
            
            javax.swing.JOptionPane.showMessageDialog(this,"Error"+e.getMessage());
            
        
        }
        
        
    }//GEN-LAST:event_facComboActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        int progID=Integer.parseInt(progIDText.getText());
        String progName=progNameText.getText();
        int duration=Integer.parseInt(durationText.getText());
        String remarks=remarksText.getText();
        
        
        try{
                DatabaseManager.updateProgram(progID, progName, duration, remarks);
                
                javax.swing.JOptionPane.showMessageDialog(this,"Recorded Updated");
                
              
                
        progIDText.setText("");
        progNameText.setText("");
        durationText.setText("");
        remarksText.setText("");
        
          getProgram();
        
                
        }
        
      
        
        
        
        
        catch(Exception e){
            e.printStackTrace();
            
            javax.swing.JOptionPane.showMessageDialog(this,"Error"+e.getMessage());
            
        }
        
        
    }//GEN-LAST:event_updateButtonActionPerformed

