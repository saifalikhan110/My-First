   private void  getProgram(){
       
       
       try{
            Vector <FacultyBean> faculty=DatabaseManager.getFaculty();
            
            for(FacultyBean bean:faculty){
            
                this.facCombo.addItem(bean);
            }
            
            
             DepartmentBean dbean=(DepartmentBean)this.deptCombo.getSelectedItem();
             
             if(dbean==null) return;
            
            Vector <ProgramBean>  v=DatabaseManager.getProgram(dbean.getDeptId());
            
                
           this.progListText.setListData(v);
            
           progList.setText("PROGRAMS:"+v.size());
            
  
       
       }
       
       catch(Exception e){
       
            e.printStackTrace();
            
            javax.swing.JOptionPane.showMessageDialog(this,"Error"+e.getMessage());
            
       }
   
   
   
   }

