    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
      
        FacultyBean bean=(FacultyBean)this.facultyList.getSelectedValue();
        if(bean==null)return;
        
        try{          
            int rows=DatabaseManager.deleteFaculty(bean.getFacId());
            javax.swing.JOptionPane.showMessageDialog(this, rows+" Records Removed.");
            getFaculty(); 
            
                        
         this.facIdText.setText("");
        this.facNameText.setText("");
        this.remarksText.setText("");
        }catch(Exception e){
           e.printStackTrace();
           javax.swing.JOptionPane.showMessageDialog(this, "Error: "+e.getMessage());
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

