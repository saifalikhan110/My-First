    private void facultyListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_facultyListValueChanged
        FacultyBean bean=(FacultyBean)this.facultyList.getSelectedValue();
        if(bean==null)return;
        
        this.facIdText.setText(""+bean.getFacId());
        this.facNameText.setText(bean.getFacName());
        this.remarksText.setText(bean.getRemarks());
    }//GEN-LAST:event_facultyListValueChanged

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String facName=facNameText.getText();
        String remarks=remarksText.getText();
        
        try{
            DatabaseManager.addFaculty(facName, remarks);
            
            javax.swing.JOptionPane.showMessageDialog(this,"Record Added");
            getFaculty();
            
        this.facIdText.setText("");
        this.facNameText.setText("");
        this.remarksText.setText("");
            
            
        
        }
        
        catch(Exception e){
        e.printStackTrace();
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        
        int id=Integer.parseInt(facIdText.getText());
        String facName=facNameText.getText();
        String remarks=remarksText.getText();
        try{
            
            DatabaseManager.updateFaculty(id, facName, remarks);
            
            getFaculty();

            
            javax.swing.JOptionPane.showMessageDialog(this,"Record Updated");
            
                        
         this.facIdText.setText("");
        this.facNameText.setText("");
        this.remarksText.setText("");
        
        }
        
        catch(Exception e){
        e.printStackTrace();
        }
    }//GEN-LAST:event_updateButtonActionPerformed

